#include <unistd.h>

void	ft_print_numbers(void)
{
	char	num;

	num = '0';
	while (num <= '9')
	{
		write(1, &num, 1);
		num++;
	}
}
/*Print numbers from 0 to 9, for each iteration shows the in the standard output
and add 1 to num.*/
