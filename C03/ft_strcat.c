char	*ft_strcat(char *dest, char *src)
{
	int	k;
	int	i;

	i = 0;
	k = 0;
	while (dest[i])
	{
		++i;
	}
	while (src[k])
	{
		dest[i] = src[k];
		++i;
		++k;
	}
	dest[i] = '\0';
	return (dest);
}
